#!/usr/bin/env python3
# Regenerate thirdParty/BURCAT-INDEX.txt -- the browsable view of the Burcat
# source pool (name, MW, dHf_298 per importable card).  The index is a VIEW,
# generated, gitignored; the .THR is the source; the catalogue only receives
# hand-curated imports.
import contextlib, importlib.util, io, sys
from pathlib import Path
root = Path(__file__).resolve().parents[2]
spec = importlib.util.spec_from_file_location("imp", root / "bin/import_nasa.py")
m = importlib.util.module_from_spec(spec)
with contextlib.redirect_stdout(io.StringIO()):
    spec.loader.exec_module(m)
buf = io.StringIO()
with contextlib.redirect_stdout(buf):
    d = m.parse_chemkin_thermo(root / "thirdParty/BURCAT.THR")
R, T = 8.314462618, 298.15
rows = []
for name, e in sorted(d.items()):
    if e["mw"] <= 0:
        continue
    rows.append((name, e["mw"], m.h_over_RT(T, e["low"]) * R * T / 1000.0))
out = ["# BURCAT.THR species index -- generated by bin/curate/burcat_index.py.",
       "# One line per importable card: name, MW [g/mol], dHf_298 [kJ/mol].",
       "# Import:  bin/import_nasa.py --thermo thirdParty/BURCAT.THR --species <name>",
       "# CAUTION: same-token cards exist (isomers/phases) -- the importer takes the",
       "# LAST card; check the .THR before promoting ring/linear or gas/liquid pairs.",
       "#", f"# {len(rows)} importable species", ""]
out += [f"{n:24s} {mw:9.4f}  {dhf:+12.1f}" for n, mw, dhf in rows]
(root / "thirdParty/BURCAT-INDEX.txt").write_text("\n".join(out) + "\n")
print(f"index regenerated: {len(rows)} species")
