#!/usr/bin/env python3
"""
genAliases -- build the component alias index (curation-time, OFFLINE).

Scans data/standards/components/*.dat for an `aliases ( ... );` field and emits
data/standards/components/ALIASES (a Choupo dict mapping each alias to the
canonical filename stem).  The C++ Database loads ALIASES ONCE and resolves an
alias to its canonical component on an exact-name MISS -- so the runtime lookup
stays O(1) and never walks the directory.

AUDITS (exit 1 on any violation -- the sibling of the duplicate-CAS guard):
  * an alias may not equal a real <stem>.dat   (exact-name always wins -> dead)
  * an alias may not map to two different canonicals (ambiguous)
  * an alias may not collide with another alias' canonical inconsistently

Run from the repo root (or anywhere -- paths are resolved from this file).
CI / bin/runTests re-runs it and fails if the committed ALIASES is stale.
"""
import os, re, sys

ROOT = os.path.dirname(os.path.dirname(os.path.dirname(os.path.abspath(__file__))))
COMP = os.path.join(ROOT, "data", "standards", "components")
OUT  = os.path.join(COMP, "ALIASES")

ALIAS_RE = re.compile(r"^\s*aliases\s*\(\s*([^)]*)\)\s*;", re.M)

def main():
    stems = {f[:-4] for f in os.listdir(COMP) if f.endswith(".dat")}
    amap = {}            # alias -> canonical stem
    errors = []
    for stem in sorted(stems):
        text = open(os.path.join(COMP, stem + ".dat"), encoding="utf-8", errors="replace").read()
        m = ALIAS_RE.search(text)
        if not m:
            continue
        for alias in m.group(1).split():
            if alias == stem:
                continue                         # self-alias: harmless, skip
            if alias in stems:
                errors.append(f"alias '{alias}' (of {stem}) equals a real component file -- exact-name wins, the alias is dead")
            if alias in amap and amap[alias] != stem:
                errors.append(f"alias '{alias}' maps to BOTH {amap[alias]} and {stem} -- ambiguous")
            amap[alias] = stem

    if errors:
        for e in errors:
            print("genAliases: ERROR:", e, file=sys.stderr)
        sys.exit(1)

    with open(OUT, "w", encoding="utf-8") as f:
        f.write("// GENERATED by bin/curate/genAliases -- DO NOT EDIT.\n")
        f.write("// Maps a friendly component alias to its canonical <stem>.dat.\n")
        f.write("// The Database resolves an alias only on an exact-name MISS (exact wins).\n\n")
        f.write("aliases\n{\n")
        for alias in sorted(amap):
            f.write(f"    {alias:<22} {amap[alias]};\n")
        f.write("}\n")
    print(f"genAliases: {len(amap)} aliases -> {len(set(amap.values()))} components, wrote {os.path.relpath(OUT, ROOT)}")

if __name__ == "__main__":
    main()
